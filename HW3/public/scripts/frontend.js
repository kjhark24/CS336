var Person = React.createClass({
  render: function() {
    return (
      <div className="person">
        <h2 className="PersonInfo">
          {this.props.firstName} {this.props.lastName} ID:{this.props.personID} Start date:{this.props.startDate}
        </h2>
      </div>
    );
  }
});



var PeopleBox = React.createClass({
  loadPeopleFromServer: function() {
    $.ajax({
      url: this.props.url,
      dataType: 'json',
      cache: false,
      success: function(people) {
        this.setState({people: people});
      }.bind(this),
      error: function(xhr, status, err) {
        console.error(this.props.url, status, err.toString());
      }.bind(this)
    });
  },
  handlePeopleSubmit: function(person) {
    var people = this.state.people;   //var comments = this.state.data;
    // Optimistically set an id on the new comment. It will be replaced by an
    // id generated by the server. In a production application you would likely
    // not use Date.now() for this and would have a more robust system in place.
    person.id = Date.now();
    var newPeople= people.concat([person]);
    this.setState({people: newPeople});
    $.ajax({
      url: this.props.url,
      dataType: 'json',
      type: 'POST',
      data: person,
      success: function(people) {
        this.setState({people: people});
      }.bind(this),
      error: function(xhr, status, err) {
        this.setState({people: person});
        console.error(this.props.url, status, err.toString());
      }.bind(this)
    });
  },
  getInitialState: function() {
    return {people: []};
  },
  componentDidMount: function() {
    this.loadPeopleFromServer();
    setInterval(this.loadPeopleFromServer, this.props.pollInterval);
  },
  render: function() {
    return (
      <div className="peopleBox">
        <h1>People</h1>
        <PeopleList people={this.state.people} />
        <PeopleForm onPeopleSubmit={this.handlePeopleSubmit} />
      </div>
    );
  }
});

var PeopleList = React.createClass({
  render: function() {
    var personNodes = this.props.people.map(function(person) {
      return (
          <Person firstName = {person.firstName} lastName = {person.lastName} personID = {person.personID} startDate = {person.startDate} key= {person.personID}>
		</Person>
      );
    });
    return (
      <div className="peopleList">
        {personNodes}
      </div>
    );
  }
});

var PeopleForm = React.createClass({
  getInitialState: function() {
    return {firstName: '', lastName: '', personID: '', startDate: ''};
  },
  handleFirstNameChange: function(e) {
    this.setState({firstName: e.target.value});
  },
  handleLastNameChange: function(e) {
    this.setState({lastName: e.target.value});
  },
  handlePersonIDChange: function(e) {
    this.setState({personID: e.target.value});
  },
  handleStartDateChange: function(e) {
    this.setState({startDate: e.target.value});
  },
  handleSubmit: function(e) {
    e.preventDefault();
    var firstName = this.state.firstName.trim();
    var lastName = this.state.lastName.trim();
    var personID = this.state.personID.trim();
    var startDate = this.state.startDate.trim();
    if (!firstName || !lastName || !personID || !startDate) {
      return;
    }
    this.props.onPeopleSubmit({firstName: firstName, lastName: lastName, personID: personID, startDate: startDate});
    this.setState({firstName: '', lastName: '', personID: '', startDate: ''});
  },
  render: function() {
    return (
      <form className="peopleForm" onSubmit={this.handleSubmit}>
        <input
          type="text"
          placeholder="First name"
          value={this.state.firstName}
          onChange={this.handleFirstNameChange}
        />
        <input
          type="text"
          placeholder="Last name"
          value={this.state.lastName}
          onChange={this.handleLastNameChange}
        />
        <input
          type="integer"
          placeholder="ID"
          value={this.state.personID}
          onChange={this.handlePersonIDChange}
        />
        <input
          type="date"
          placeholder="Start Date"
          value={this.state.startDate}
          onChange={this.handleStartDateChange}
        />
        <input type="submit" value="Post" />
      </form>
    );
  }
});

ReactDOM.render(
  <PeopleBox url="/api/people" pollInterval={2000} />,
  document.getElementById('content')
);
